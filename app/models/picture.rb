class Picture < ActiveRecord::Base

  #============================================================================
  # Associations
  #============================================================================
  belongs_to :album
  mount_uploader :location, PictureUploader

  #============================================================================
  # Lambdas
  #============================================================================
  default_scope -> { order(created_at: :desc) } # Retreive in LIFO order

  #============================================================================
  # Validations
  #============================================================================
  validates :album_id, presence: true

end
